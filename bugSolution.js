function foo(a, b) {
  if (a === null || b === null) {
    return null; // Handle null values
  }
  return a + b;
}

console.log(foo(null, 5)); // Output: null
console.log(foo(5, null)); // Output: null
console.log(foo(5, 5)); // Output: 10
//The solution is to explicitly handle the null values using strict equality.  Using strict equality prevents the unexpected behavior caused by loose comparison.